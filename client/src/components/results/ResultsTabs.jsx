// client/src/components/results/ResultsTabs.jsx
import { useState } from 'react';
import SecurityTab from './SecurityTab.jsx';
import DocumentationTab from './DocumentationTab.jsx';
import ArchitectureTab from './ArchitectureTab.jsx';
import useAgentStore from '../../store/agentStore.js';

const ResultsTabs = () => {
  const { securitySummary, writerResult, architectureResult, compilationResult, reset } = useAgentStore();
  const bugs = securitySummary?.bugs || [];
  const documentation = writerResult;
  const refactors = architectureResult?.refactors || [];
  const finalResults = compilationResult;
  const [activeTab, setActiveTab] = useState('security');

  const tabs = [
    {
      id: 'security',
      label: 'Security',
      icon: '🛡️',
      badge: bugs.length,
      badgeColor: bugs.length > 0 ? 'bg-red-500' : 'bg-gray-600',
    },
    {
      id: 'documentation',
      label: 'Documentation',
      icon: '📄',
      badge: documentation ? 'Ready' : null,
      badgeColor: 'bg-green-500',
    },
    {
      id: 'architecture',
      label: 'Architecture',
      icon: '🏗️',
      badge: refactors.length,
      badgeColor: 'bg-blue-500',
    },
  ];

  const duration = finalResults?.duration || finalResults?.timing?.total;

  const handleReset = () => {
    if (confirm('Start a new analysis? This will clear all current results.')) {
      reset();
    }
  };

  const renderTabContent = () => {
    switch (activeTab) {
      case 'security':
        return <SecurityTab />;
      case 'documentation':
        return <DocumentationTab />;
      case 'architecture':
        return <ArchitectureTab />;
      default:
        return <SecurityTab />;
    }
  };

  return (
    <div className="bg-[#12141f] border border-[#2d3748] rounded-xl overflow-hidden">
      {/* Header */}
      <div className="px-6 py-4 border-b border-[#2d3748] flex flex-col md:flex-row md:items-center md:justify-between gap-4">
        <div>
          <h2 className="text-xl font-semibold text-white flex items-center gap-2">
            <span>📊</span> Analysis Results
          </h2>
          <p className="text-sm text-gray-400 mt-1">
            Generated by 3 specialist AI agents
          </p>
        </div>
        <div className="flex items-center gap-3">
          {duration && (
            <span className="bg-[#1a1d2e] text-gray-300 px-3 py-1.5 rounded-lg text-sm border border-[#2d3748] flex items-center gap-2">
              <span>⏱</span> Completed in {typeof duration === 'number' ? `${(duration / 1000).toFixed(1)}s` : duration}
            </span>
          )}
          <button
            onClick={handleReset}
            className="bg-[#1a1d2e] hover:bg-[#252a3e] text-gray-300 px-4 py-1.5 rounded-lg text-sm border border-[#2d3748] transition-colors"
          >
            New Analysis
          </button>
        </div>
      </div>

      {/* Tab Bar */}
      <div className="px-6 border-b border-[#2d3748]">
        <div className="flex gap-1">
          {tabs.map((tab) => (
            <button
              key={tab.id}
              onClick={() => setActiveTab(tab.id)}
              className={`relative px-4 py-3 text-sm font-medium transition-colors flex items-center gap-2 ${activeTab === tab.id
                  ? 'text-white'
                  : 'text-gray-400 hover:text-gray-200'
                }`}
            >
              <span>{tab.icon}</span>
              <span>{tab.label}</span>
              {tab.badge !== null && tab.badge !== 0 && (
                <span className={`${tab.badgeColor} text-white text-xs px-1.5 py-0.5 rounded-full min-w-[20px] text-center`}>
                  {tab.badge}
                </span>
              )}
              {/* Active indicator */}
              {activeTab === tab.id && (
                <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-indigo-500" />
              )}
            </button>
          ))}
        </div>
      </div>

      {/* Tab Content */}
      <div className="p-6">
        <div
          key={activeTab}
          className="animate-fadeIn"
          style={{
            animation: 'fadeIn 0.2s ease-in-out',
          }}
        >
          {renderTabContent()}
        </div>
      </div>

      {/* Inline animation style */}
      <style>{`
        @keyframes fadeIn {
          from {
            opacity: 0;
            transform: translateY(10px);
          }
          to {
            opacity: 1;
            transform: translateY(0);
          }
        }
        .animate-fadeIn {
          animation: fadeIn 0.2s ease-in-out;
        }
      `}</style>
    </div>
  );
};

export default ResultsTabs;